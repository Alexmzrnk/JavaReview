package com.syntax.review3;

public class Loops {

    public static void main(String[] args) {

        /* for loops we have: for, while, do while, enhanced or advanced for loop*/

        int time = 9;
        while (time>7) {
            System.out.println("Hi");
            time--; // increment operator
        }

        System.out.println("Value of time = "+time);


    }
}
